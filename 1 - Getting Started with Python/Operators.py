### Arithmetic

#Addition
print(2+3.5)
print(3.5+6.9)
print(True + False)
print('P '+ 'Sreyanshu') #concatination not addition
print('------------')

#Subtraction
print(2-3.5)
print(3.50-6.9)
print(False - True)
print('------------')

#Multiplication
print(4*7.9)
print("PSa "*3)
print('------------')

#Division
print(3/2)
print(3.5/3)
print('------------')

#Floor Division
print(3.6//2)
print(7//3)
print('------------')

#Modulo
print(3%2)
print(3.5%3)
print('------------')

#Exponentiation
print(3**2)
print(3.5**3)
print('------------')

#abs
print(abs(-4.5))
#round
print(round(3.231124,3))
#pow
print(pow(3,5))

